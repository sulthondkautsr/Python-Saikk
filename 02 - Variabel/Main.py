# Variabel adalah tempat menyimpan data

# Menaruh / Assignment nilai
a = 5
b = 3
panjang = 2020

# Pemanggilan pertama
print("Nilai a = ", a)
print("Nilai b = ", b)
print("Nilai panjang = ", panjang)

# Penamaan
nilai_x = 5  # Dengan menggunakan underscore
ratus5 = 500
nilaiZ = 15.8

# Pemanggilan kedua
print("Nilai a = ", a)
a = 8
print("Nilai a = ", a)

# Assignment indirect, langsung ke variabel sebelumnya
b = a
print("Nilai b = ", b)
